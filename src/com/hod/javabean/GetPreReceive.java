package com.hod.javabean;

public class GetPreReceive {

	private int roomId;
	private String roomName;
	private String clientName;
	private double roomSize;
	private double prMonths;
	private double prFactor;
	private double prMoney;
	public int getRoomId() {
		return roomId;
	}
	public void setRoomId(int roomId) {
		this.roomId = roomId;
	}
	public String getRoomName() {
		return roomName;
	}
	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public double getRoomSize() {
		return roomSize;
	}
	public void setRoomSize(double roomSize) {
		this.roomSize = roomSize;
	}
	public double getPrMonths() {
		return prMonths;
	}
	public void setPrMonths(double prMonths) {
		this.prMonths = prMonths;
	}
	public double getPrFactor() {
		return prFactor;
	}
	public void setPrFactor(double prFactor) {
		this.prFactor = prFactor;
	}
	public double getPrMoney() {
		return prMoney;
	}
	public void setPrMoney(double prMoney) {
		this.prMoney = prMoney;
	}
}
